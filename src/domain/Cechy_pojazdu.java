package domain;

public class Cechy_pojazdu {
	
	private String kolor;
	private String predkosc;
	private String przyspieszenie;
	private Pojazd pojazd;
	
	public String getKolor() {
		return kolor;
	}
	public void setKolor(String kolor) {
		this.kolor = kolor;
	}
	public String getPredkosc() {
		return predkosc;
	}
	public void setPredkosc(String predkosc) {
		this.predkosc = predkosc;
	}
	public String getPrzyspieszenie() {
		return przyspieszenie;
	}
	public void setPrzyspieszenie(String przyspieszenie) {
		this.przyspieszenie = przyspieszenie;
	}
	public Pojazd getPojazd() {
		return pojazd;
	}
	public void setPojazd(Pojazd pojazd) {
		this.pojazd = pojazd;
	}
	
}
